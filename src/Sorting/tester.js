function compareArrays(received, expected) {
	if (expected.length !== received.length) return false;
	for (let i = 0; i < expected.length; i++) {
		if (received[i] !== expected[i]) return false;
	}

	return true;
}

function testRunner(sortFn) {
	testArrays.forEach((batch) => {
		let start = performance.now();
		let results = sortFn(batch.test);
		if (compareArrays(results, batch.expected)) {
			console.log(`Test passed for ${batch.id} Array!`);
		} else {
			console.error(
				`${batch.id} Array was not properly sorted.
                Expected: ${batch.expected}
                Received: ${results}\n`
			);
		}
		let end = performance.now();
		console.log(`Took ${(end - start).toFixed(3)} milliseconds to complete.`);
	});
}

